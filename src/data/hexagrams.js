/**
 * 通过卦名查找卦象
 */
export function getHexagram(name) {
  return HEXAGRAMS.find(h => h.name === name)
}

/**
 * 通过卦序查找卦象
 */
export function getHexagramByNumber(n) {
  return HEXAGRAMS.find(h => h.number === n)
}

